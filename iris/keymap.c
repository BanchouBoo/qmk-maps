bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        // LAYER + mod keys to pick different layers
        case LAYER: {
            if (record->event.pressed) {
                uint8_t mods = get_mods();
                uint8_t layer = _DVORAK;
                if (mods == MOD_BIT(KC_LSFT)) {
                    layer = _QWERTY;
                } else if (mods == MOD_BIT(KC_LALT)) {
                    layer = _GAMING;
                } else if (mods == MOD_BIT(KC_LCTL)) {
                    layer = _STENO;
                }
                default_layer_set(1 << layer);
            }
        } return false;
        // BOOT + control alt and shift to enter bootloader
        case BOOT: {
            if (record->event.pressed) {
                uint8_t mods = get_mods();
                if (mods == (MOD_BIT(KC_LSFT) | MOD_BIT(KC_LALT) | MOD_BIT(KC_LCTL))) {
                    reset_keyboard();
                }
            }
        } return false;
    }
    return true;
}

void matrix_init_user() {
    steno_set_mode(STENO_MODE_GEMINI);
}
